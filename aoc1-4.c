U4 aoc3_n = countof(aoc3_input);

U4
aoc3_priority(char ch)
{
    if ('a' <= ch && ch <= 'z')
    {   return ch - 'a' + 1;   }
    else if ('A' <= ch && ch <= 'Z')
    {   return ch - 'A' + 27;   }
    else return 0;
}


typedef uint8_t U1;
U4
aoc3(char const **rucks, U4 rucks_n)
{
    U4 result = 0;

    for (U4 ruck_i = 0; ruck_i < rucks_n; ++ruck_i)
    {
        U1          priorities_hit[53] = {0};
        char const *ruck               = rucks[ruck_i];
        U4          cpt_size           = strlen(ruck) / 2;
        for (U4 cpt_i = 0; cpt_i < cpt_size; ++cpt_i)
        {
            char ch             = ruck[cpt_i];
            U4   pri            = aoc3_priority(ch);
            priorities_hit[pri] = 1;
        }

        for (U4 cpt_i = 0; cpt_i < cpt_size; ++cpt_i)
        {
            char ch  = ruck[cpt_size + cpt_i];
            U4   pri = aoc3_priority(ch);
            if (priorities_hit[pri])
            {
                result += pri;
                break;
            }
        }
    }

    return result;
}


U4
aoc3b(char const **rucks, U4 rucks_n)
{
    U4 result = 0;
    enum { group_n = 3 };

    for (U4 ruck_i = 0; ruck_i < rucks_n; ruck_i+=group_n)
    {
        U1 hit[3][53] = {0};

        for (U4 group_i = 0; group_i < group_n; ++group_i)
        {
            char const *ruck      = rucks[ruck_i+group_i];
            U4          ruck_size = strlen(ruck);
            for (U4 cpt_i = 0; cpt_i < ruck_size; ++cpt_i)
            {
                char ch             = ruck[cpt_i];
                U4   pri            = aoc3_priority(ch);
                hit[group_i][pri] = 1;
            }
        }

        int found = 0;
        for (U4 pri_i = 0; pri_i < countof(hit[0]); ++pri_i)
        {
            U1 pri_hit[3] = {
                hit[0][pri_i],
                hit[1][pri_i],
                hit[2][pri_i],
            };
            if (pri_hit[0] & pri_hit[1] & pri_hit[2])
            {
                result += pri_i;
                found = 1;
                break;
            }
        }

        if (! found) { __debugbreak(); };
    }

    return result;
}








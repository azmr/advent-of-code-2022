U4
aoc1(U4 *calories, U4 calories_n)
{
    U4 elf_i                = 0;
    U4 current_elf_calories = 0;
    U4 max_elf_calories     = 0;
    for (U4 food_i = 0; food_i < calories_n; ++food_i)
    {
        U4 cal = calories[food_i];
        if (cal == 0)
        {
            ++elf_i;
            if (current_elf_calories > max_elf_calories)
            {   max_elf_calories = current_elf_calories;   }
            current_elf_calories = 0;
        }
        else
        {
            current_elf_calories += cal;
        }
    }

    return max_elf_calories;
}


U4 aoc2(char const **strats, U4 strats_n)
{
    U4 total_score = 0;

    for (U4 i = 0; i < strats_n; ++i)
    {
        Shape opp = opponent_shape(strats[i][0]);
        Shape you = your_shape(strats[i][1]);

        U4 outcome_score = outcome_scores[opp][you];
        U4 shape_score   = shape_scores[you];
        U4 round_score   = outcome_score + shape_score;
        total_score     += round_score;
    }

    return total_score;
}

typedef enum { Loss, Draw, Win, Outcome_Count } Outcome;
Outcome outcome_aim(char c)
{   return c - 'X';   }

//                       opp          aim
Shape shape_from_outcome[Shape_Count][Outcome_Count] = {
    //             L         D         W
    [Rock]     = { Scissors, Rock,     Paper    },
    [Paper]    = { Rock,     Paper,    Scissors },
    [Scissors] = { Paper,    Scissors, Rock     },
};

U4 aoc2b(char const **strats, U4 strats_n)
{
    U4 total_score = 0;

    for (U4 i = 0; i < strats_n; ++i)
    {
        char const *strat = strats[i];
        Shape   opp = opponent_shape(strat[0]);
        Outcome aim = outcome_aim(strat[1]);
        Shape   you = shape_from_outcome[opp][aim];

        U4 outcome_score = outcome_scores[opp][you];
        U4 shape_score   = shape_scores[you];
        U4 round_score   = outcome_score + shape_score;
        total_score     += round_score;
    }

    return total_score;
}

